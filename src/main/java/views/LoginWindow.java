    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        RestTemplate restTemplate = new RestTemplate();
        String usuario=usuariotext.getText();
        String password=passwordtext.getText();
        
        String url = "http://localhost:8080/Evaluator/Login?usuario="+usuario+"&password="+password; 
        HttpHeaders headers = new HttpHeaders();
        headers.set("Content-Type", "application/json");
        
         ResponseEntity<Evaluator> response = restTemplate.exchange(
                url,
                HttpMethod.GET,
                null,
                new ParameterizedTypeReference<Evaluator>() {});

        Evaluator resultados = response.getBody();

        // Imprimir los resultados
        if (resultados != null) {
            dispose();
            SelectorAreasWindow window=new SelectorAreasWindow(resultados.getId_usuario());
            window.setVisible(true);
            //System.out.println("ID: " + resultados.getId_usuario());
        }else{
            alerta.setText("Usuario o contrase√±a no validos");
        }
                
                
            
        
        
       
    }//GEN-LAST:event_LoginActionPerformed


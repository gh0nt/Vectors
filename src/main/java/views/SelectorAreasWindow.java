    public SelectorAreasWindow(int id_usuario) {
        this.id_usuario=id_usuario;
        setSize(500, 600);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        initComponents();
        RestTemplate restTemplate = new RestTemplate();
        String url = "http://localhost:8080/AreasAsignadas/Usuario?id_usuario="+id_usuario; 
        HttpHeaders headers = new HttpHeaders();
        headers.set("Content-Type", "application/json");
        
         ResponseEntity<List<AreasAsignadas>> response = restTemplate.exchange(
                url,
                HttpMethod.GET,
                null,
                new ParameterizedTypeReference<List<AreasAsignadas>>() {});

        List<AreasAsignadas> resultados = response.getBody();

        if (resultados != null) {
            for (AreasAsignadas areasAsignadas : resultados) {
                ComboAreas.addItem(String.valueOf(areasAsignadas.getId_areasasignadas()));
            }
        }
        else{
            alerta.setText("El Usuario no tiene areas asignadas");
        }
        
        
    }

